tempc = input("Wpisz temperaturę w stopniach celcjusza: ")
tempk = int(tempc) + 273.15
tempf = int(tempc) * 9/5 + 32
print(f"Temperatura w stopniach celcjusza wynosi: {tempc} \n kelvina: {tempk} \n fahrenheita {tempf}")

